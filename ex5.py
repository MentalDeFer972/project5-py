def sum(a, b):
    '''This function return the sum of two numbers.
          Parameters:
                    a (int): first number
                    b (int): second number
          Returns:
                    int: sum of two numbers'''
    return a + b


print(sum.__doc__)


def substraction(a, b):
    '''This function return the substract of two numbers.
          Parameters:
                    a (int): first number
                    b (int): second number
          Returns:
                    int: substract of two numbers'''
    return a - b


print(substraction.__doc__)
